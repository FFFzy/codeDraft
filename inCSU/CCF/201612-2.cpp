#include<iostream>
using namespace std;

int main(){
	double T;
	cin>>T;
	
	double S;
	if(T<=3500) S=T;
	else if(T <= 3500+1500*0.97) 
		S = T+(T-3500)/0.97*0.03;
	else if(T <= 3500+1500*0.97+3000*0.9) 
		S = T+1500*0.03+(T-3500-1500*0.97)/0.9*0.1;
	else if(T <= 3500+1500*0.97+3000*0.9+4500*0.8)
		S = T+1500*0.03+3000*0.1+(T-3500-1500*0.97-3000*0.9)/0.8*0.2; 
	else if(T <= 3500+1500*0.97+3000*0.9+4500*0.8+26000*0.75)
		S = T+1500*0.03+3000*0.1+4500*0.2+(T-3500-1500*0.97-3000*0.9-4500*0.8)/0.75*0.25;
	else if(T <= 3500+1500*0.97+3000*0.9+4500*0.8+26000*0.75+20000*0.7)
		S = T+1500*0.03+3000*0.1+4500*0.2+26000*0.25+(T-3500-1500*0.97-3000*0.9-4500*0.8-26000*0.75)/0.7*0.3;
	else if(T <= 3500+1500*0.97+3000*0.9+4500*0.8+26000*0.75+20000*0.7+25000*0.65)
		S = T+1500*0.03+3000*0.1+4500*0.2+26000*0.25+20000*0.3+(T-3500-1500*0.97-3000*0.9-4500*0.8-26000*0.75-20000*0.7)/0.65*0.35;
	else 
		S = T+1500*0.03+3000*0.1+4500*0.2+26000*0.25+20000*0.3+25000*0.35+(T-3500-1500*0.97-3000*0.9-4500*0.8-26000*0.75-20000*0.7-25000*0.65)/0.55*0.45;

	cout<<S;
	return 0;
} 
